export const useTranslation = (language: string = 'en') => {
  const t = (key: string) => {
    return translations[language as keyof typeof translations]?.[key as keyof typeof translations.en] ||
           translations.en[key as keyof typeof translations.en] ||
           key;
  };

  return { t };
};

// Language detection and management
export const getBrowserLanguage = (): string => {
  if (typeof window === 'undefined') return 'en';

  const browserLang = navigator.language || 'en';
  const langCode = browserLang.split('-')[0];

  // Map browser language to our supported languages
  const supportedLanguages = {
    'en': 'en',
    'hi': 'hi',
    'ta': 'ta',
    'bn': 'bn',
    'te': 'te'
  };

  return supportedLanguages[langCode as keyof typeof supportedLanguages] || 'en';
};

// Voice language mapping
export const getVoiceLanguage = (uiLanguage: string): string => {
  const voiceMappings = {
    'en': 'en-US',
    'hi': 'hi-IN',
    'ta': 'ta-IN',
    'bn': 'bn-IN',
    'te': 'te-IN'
  };

  return voiceMappings[uiLanguage as keyof typeof voiceMappings] || 'en-US';
};
